function extractBodyShell(source) {
  const bodyMatch = source.match(/<body[^>]*>([\s\S]*?)<\/body>/i);
  if (!bodyMatch) throw new Error("Cannot find <body> shell in index.html.");
  return bodyMatch[1].replace(/<script\s+type=["']module["']\s+src=["']\/src\/main\.js["']><\/script>\s*/i, "").trim();
}

function extractBuiltAsset(indexHtml, extension) {
  const pattern = new RegExp(`(?:src|href)=["']([^"']+\\.(${extension}))["']`, "i");
  const match = indexHtml.match(pattern);
  if (!match) throw new Error(`Cannot find built .${extension} asset in dist/index.html. Run npm run build:app first.`);
  return path.join(projectRoot, "dist", match[1].replace(/^\/+/, ""));
}

function standaloneRuntimeBootstrap(appShellHtml) {
  return `/* WebSheet standalone bootstrap ${STANDALONE_RUNTIME_VERSION}. Generated by scripts/install-standalone-runtime.mjs. */
(async function () {
  "use strict";
  var currentScript = document.currentScript;
  var modelElement = document.getElementById("websheet-model");
  window.__WEBSHEET_STANDALONE__ = true;
  if (!window.__WEBSHEET_LOCAL_RUNTIME_STATUS__ && window.__WEBSHEET_RUNTIME_SOURCE__ === "remote") {
    window.__WEBSHEET_LOCAL_RUNTIME_STATUS__ = "missing";
  }
  function clearWorkbookModelScriptText() {
    if (!modelElement) return;
    try {
      modelElement.textContent = "";
      modelElement.setAttribute("data-cws-runtime-cleared", "true");
      window.__WEBSHEET_STANDALONE_MODEL_SCRIPT_CLEARED__ = true;
    } catch (error) {}
  }
  function workbookPayload(value) {
    var seen = [];
    var seenText = [];
    var queue = [value];
    while (queue.length) {
      var candidate = queue.shift();
      if (!candidate) continue;
      if (typeof candidate === "string") {
        var text = candidate.trim();
        if (!text || seenText.indexOf(text) >= 0 || !/^[\\[{]/.test(text)) continue;
        seenText.push(text);
        try {
          queue.push(JSON.parse(text));
        } catch (error) {}
        continue;
      }
      if (typeof candidate !== "object" || seen.indexOf(candidate) >= 0) continue;
      seen.push(candidate);
      if (Array.isArray(candidate.sheets) && candidate.sheets.length) return candidate;
      ["model", "workbook", "workbookModel", "webSheetWorkbook", "data", "payload"].forEach(function(key) {
        if (candidate[key]) queue.push(candidate[key]);
      });
    }
    return null;
  }
  function base64ToBytes(value) {
    var binary = atob(String(value || ""));
    var bytes = new Uint8Array(binary.length);
    for (var index = 0; index < binary.length; index += 1) {
      bytes[index] = binary.charCodeAt(index);
    }
    return bytes;
  }
  function bytesToBase64(bytes) {
    var binary = "";
    var chunkSize = 0x8000;
    for (var index = 0; index < bytes.length; index += chunkSize) {
      var chunk = bytes.subarray(index, index + chunkSize);
      binary += String.fromCharCode.apply(null, chunk);
    }
    return btoa(binary);
  }
  async function gunzipBase64Bytes(value) {
    if (typeof DecompressionStream !== "function" || typeof Response !== "function" || typeof Blob !== "function") {
      throw new Error("圧縮された CWS HTML を読み込むには、gzip 解凍に対応したブラウザーが必要です。");
    }
    var stream = new Blob([base64ToBytes(value)]).stream().pipeThrough(new DecompressionStream("gzip"));
    return new Uint8Array(await new Response(stream).arrayBuffer());
  }
  async function gunzipBase64Text(value) {
    return new TextDecoder().decode(await gunzipBase64Bytes(value));
  }
  async function decodeCompressedImageAssets(model) {
    var assets = model && model.assets && model.assets.images;
    if (!assets || typeof assets !== "object") return model;
    var keys = Object.keys(assets);
    for (var index = 0; index < keys.length; index += 1) {
      var asset = assets[keys[index]];
      if (!asset || asset.dataEncoding !== "gzip-base64" || typeof asset.data !== "string") continue;
      asset.data = bytesToBase64(await gunzipBase64Bytes(asset.data));
      delete asset.dataEncoding;
      delete asset.uncompressedByteLength;
    }
    return model;
  }
  async function decodeWorkbookModelScriptPayload(value) {
    var decoded = value;
    if (value && typeof value === "object" && value.encoding === "gzip-base64" && typeof value.payload === "string") {
      decoded = JSON.parse(await gunzipBase64Text(value.payload));
    }
    return await decodeCompressedImageAssets(decoded);
  }
  if (modelElement && String(modelElement.textContent || "").trim()) {
    try {
      var parsedModelPayload = JSON.parse(modelElement.textContent || "{}");
      window.__WEBSHEET_STANDALONE_MODEL_RAW__ = await decodeWorkbookModelScriptPayload(parsedModelPayload);
      var workbookModel = workbookPayload(window.__WEBSHEET_STANDALONE_MODEL_RAW__);
      if (workbookModel) {
        window.__WEBSHEET_STANDALONE_MODEL__ = workbookModel;
        clearWorkbookModelScriptText();
      }
    } catch (error) {
      window.__WEBSHEET_STANDALONE_MODEL_ERROR__ = error && error.message ? error.message : String(error);
      var boot = document.getElementById("websheetStandaloneBoot");
      if (boot) {
        var detail = boot.querySelector("div") || boot;
        detail.textContent = "HTML ワークブックのデータを読み込めませんでした。WebSheet を起動中...";
      }
    }
  }
  document.body.innerHTML = ${JSON.stringify(appShellHtml)};
  var runtimeBase = currentScript && currentScript.src ? currentScript.src : document.baseURI;
  await new Promise(function(resolve, reject) {
    var script = document.createElement("script");
    var appUrl = new URL("websheet-app.js", runtimeBase);
    appUrl.searchParams.set("v", ${JSON.stringify(STANDALONE_RUNTIME_VERSION)});
    appUrl.searchParams.set("b", ${JSON.stringify(runtimeBuildId)});
    script.src = appUrl.href;
    script.async = false;
    script.onload = resolve;
    script.onerror = function(){ reject(new Error("WebSheet アプリ ランタイムを読み込めませんでした。")); };
    document.head.appendChild(script);
  });
})();\n`;
}

